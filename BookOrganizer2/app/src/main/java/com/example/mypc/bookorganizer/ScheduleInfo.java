package com.example.mypc.bookorganizer;

/**
 *
 */

public class ScheduleInfo {
    private long date;
    private int from;
    private int to;

    public long getDate() {
        return date;
    }

    public void setDate(long date) {
        this.date = date;
    }

    public int getFrom() {
        return from;
    }

    public void setFrom(int from) {
        this.from = from;
    }

    public int getTo() {
        return to;
    }

    public void setTo(int to) {
        this.to = to;
    }
}
